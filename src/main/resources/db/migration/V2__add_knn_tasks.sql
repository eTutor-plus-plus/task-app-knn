CREATE TABLE knn_tasks (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    task_id BIGINT NOT NULL UNIQUE REFERENCES task(id) ON DELETE CASCADE,
    k INTEGER NOT NULL,
    metric VARCHAR(32) NOT NULL,
    num_train INTEGER NOT NULL,
    x_min INTEGER NOT NULL,
    x_max INTEGER NOT NULL,
    y_min INTEGER NOT NULL,
    y_max INTEGER NOT NULL,
    num_test INTEGER NOT NULL,
    x_label VARCHAR(32) NOT NULL,
    y_label VARCHAR(32) NOT NULL,
    tiebreaker VARCHAR(32) NOT NULL,
    aufgaben_png_base64_de TEXT,
    loesung_png_base64_de TEXT,
    aufgaben_png_base64_en TEXT,
    loesung_png_base64_en TEXT,
    train_points_json TEXT ,
    train_labels_json TEXT NOT NULL,
    test_points_json TEXT,
    created_date TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    last_modified_date TIMESTAMP WITH TIME ZONE DEFAULT CURRENT_TIMESTAMP
);
